package com.vincent.es.entity;

public class Course {
    private String name; // 課程名稱
    private int point;   // 學分數

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPoint() {
        return point;
    }

    public void setPoint(int point) {
        this.point = point;
    }
}
